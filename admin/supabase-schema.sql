drop policy if exists "public all packages" on packages;
drop policy if exists "public all classes" on classes;
drop policy if exists "public all payments" on payments;
drop policy if exists "public all route_logs" on route_logs;
drop policy if exists "public all messages" on messages;
drop policy if exists "public all schedule_events" on schedule_events;

